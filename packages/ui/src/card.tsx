import * as React from "react"
import { cn } from "@repo/lib"

// A custom Card component with the "Glassmorphism" effect
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-2xl border border-white/10 bg-white/5 p-6 shadow-lg backdrop-blur-md",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

